TITLE	MACRO(EXE) PROGRAMMING
PAGE 60,132

STRING MACRO DATA1
	MOV AH,09H
	MOV DX,OFFSET DATA1
	INT 21H
	ENDM

.MODEL SMALL
.STACK 64
.DATA
	DATA1 DB 'THIS IS A SAMPLE PROGRAM USING MACRO $'
	DATA2 DB 0DH,0AH,0DH,0AH,'DISPLAY ANOTHER ONE USING MACRO $'
	
.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX

	STRING DATA1
	STRING DATA2

	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN
